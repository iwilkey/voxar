package dev.iwilkey.voxar.gui;

/**
 * Conceptual "Anchors" that describe relative screen position depending on current window dimensions.
 * @author iwilkey
 */
public enum Anchor {
	TOP_LEFT,
	TOP_CENTER,
	TOP_RIGHT,
	CENTER_LEFT,
	CENTER,
	CENTER_RIGHT,
	BOTTOM_LEFT,
	BOTTOM_CENTER,
	BOTTOM_RIGHT
}
